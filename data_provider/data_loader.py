class Dataset_M4(Dataset):
    def __init__(self, root_path, flag='pred', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=False, timeenc=0, freq='15min',
                 seasonal_patterns='Yearly'):
        # size [seq_len, label_len, pred_len]
        # init
        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.seasonal_patterns = seasonal_patterns
        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        # M4Dataset.initialize()
        if self.seasonal_patterns == 'Yearly':
            self.m4_meta = M4Meta.seasonal_patterns[0]
        elif self.seasonal_patterns == 'Quarterly':
            self.m4_meta = M4Meta.seasonal_patterns[1]
        elif self.seasonal_patterns == 'Monthly':
            self.m4_meta = M4Meta.seasonal_patterns[2]
        elif self.seasonal_patterns == 'Weekly':
            self.m4_meta = M4Meta.seasonal_patterns[3]
        elif self.seasonal_patterns == 'Daily':
            self.m4_meta = M4Meta.seasonal_patterns[4]
        elif self.seasonal_patterns == 'Hourly':
            self.m4_meta = M4Meta.seasonal_patterns[5]
        else:
            raise Exception('Unsupported seasonal patterns')

        self.timeseries = [ts for ts in M4Dataset(self.root_path).load(self.seasonal_patterns)]
        self.train_data = [ts.values for ts in self.timeseries]
        self.test_data = [ts.values for ts in self.timeseries]

    def __getitem__(self, index):
        insample = np.zeros((self.seq_len, 1))
        insample_mask = np.zeros((self.seq_len, 1))
        outsample = np.zeros((self.pred_len + self.label_len, 1))
        outsample_mask = np.zeros((self.pred_len + self.label_len, 1))

        sampled_timeseries = self.timeseries[index]
        cut_point = np.random.randint(low=max(1, len(sampled_timeseries) - self.m4_meta.horizont),
                                      high=len(sampled_timeseries),
                                      size=1)[0]

        insample_window = sampled_timeseries[max(0, cut_point - self.seq_len):cut_point]
        insample[-len(insample_window):, 0] = insample_window
        insample_mask[-len(insample_window):, 0] = 1.0
        outsample_window = sampled_timeseries[
                           cut_point - self.label_len:min(len(sampled_timeseries), cut_point + self.pred_len)]
        outsample[:len(outsample_window), 0] = outsample_window
        outsample_mask[:len(outsample_window), 0] = 1.0
        return insample, outsample, insample_mask, outsample_mask

    def __len__(self):
        return len(self.timeseries)

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)

    def last_insample_window(self):
        """
        The last window of insample size of all timeseries.
        This function does not support batching and does not reshuffle timeseries.

        :return: Last insample window of all timeseries. Shape "timeseries, insample size"
        """
        insample = np.zeros((len(self.timeseries), self.seq_len))
        insample_mask = np.zeros((len(self.timeseries), self.seq_len))
        for i, ts in enumerate(self.timeseries):
            ts_last_window = ts.values[-self.seq_len:]
            insample[i, -len(ts_last_window):] = ts_last_window
            insample_mask[i, -len(ts_last_window):] = 1.0
        return insample, insample_mask